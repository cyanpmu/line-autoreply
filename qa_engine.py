def match_question(user_input, min_score=5):
    """학생 질문을 입력받아 가장 적합한 Q&A를 찾아 반환"""
    if not user_input or len(user_input.strip()) < 2:
        return []

    lower = user_input.lower()
    tokens = [t for t in lower.replace("？","").replace("?","").replace("！","").replace("!","").split() if len(t) >= 1]

    results = []
    for qa in QA_DATA:
        score = 0
        # 키워드 매칭 (가장 높은 가중치)
        for kw in qa["kw"]:
            if kw.lower() in lower:
                score += 10
        # 토큰 매칭 (질문 텍스트와 비교)
        for tok in tokens:
            if tok in qa["q_ja"].lower():
                score += 3
            if tok in qa["q_ko"].lower():
                score += 3
        # 카테고리 매칭
        for tok in tokens:
            if tok in qa["cat"]:
                score += 5

        if score >= min_score:
            results.append({**qa, "score": score})

    results.sort(key=lambda x: x["score"], reverse=True)
    return results


def get_best_reply(user_input, reply_lang="ja"):
    """최고 매칭 답변을 반환. 매칭 없으면 None"""
    matches = match_question(user_input)
    if not matches:
        return None, 0

    best = matches[0]
    answer = best["a_ja"] if reply_lang == "ja" else best["a_ko"]
    return answer, best["score"]


