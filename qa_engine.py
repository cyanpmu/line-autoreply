def match_question(text, min_score=8):
    if not text or len(text.strip()) < 2:
        return []
    lower = text.lower()
    results = []
    for qa in QA_DATA:
        score = sum(10 for kw in qa["kw"] if kw.lower() in lower)
        if score >= min_score:
            results.append({"id": qa["id"], "answer": qa["a"], "score": score})
    results.sort(key=lambda x: x["score"], reverse=True)
    return results

def get_best_reply(text):
    m = match_question(text)
    return (m[0]["answer"], m[0]["score"]) if m else (None, 0)
